#include "HumanA.hpp"
#include "HumanB.hpp"
#include <string>


int main()
{
	{
		Weapon club = Weapon("crude spiked club"); //грубая дубина с шипами
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}

	return 0;
}